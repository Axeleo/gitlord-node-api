function findUsers(nodes) {
  var users = {}
  for (let i = 0; i < nodes.length; i++) {
    let username = nodes[i].participants.nodes[0].login
    users[username] = 0
  }
  for (let i = 0; i < nodes.length; i++) {
    let username = nodes[i].participants.nodes[0].login
    let deletions = nodes[i].deletions
    let additions = nodes[i].additions
    users[username] += additions + deletions
  }
  return users
}




